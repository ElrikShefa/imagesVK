import UIKit

extension UIColor {
    
    static func tintBlue() -> UIColor {
        return UIColor(red: 0.973, green: 0.973, blue: 0.973, alpha: 0.92)
    }
    
    static func tintGray() -> UIColor {
        return UIColor(red: 0.549, green: 0.549, blue: 0.549, alpha: 1)
    }

}
